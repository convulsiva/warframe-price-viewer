export type TranslationKey = keyof typeof en;

export function translate(language: AppLanguage, key: TranslationKey, variables: Record<string, string | number> = {}): string {
  let value: string = (language === "ru" ? ru : en)[key];
  for (const [name, replacement] of Object.entries(variables)) {
    value = value.replaceAll(`{${name}}`, String(replacement));
  }
  return value;
}

export function useI18n() {
  const language = useSettingsStore((state) => state.language);
  const t = useCallback(
    (key: TranslationKey, variables?: Record<string, string | number>) => translate(language, key, variables),
    [language]
  );
  return {
    language,
    locale: language === "ru" ? "ru-RU" : "en-US",
    t
  };
}

export function currentLanguage(): AppLanguage {
  return useSettingsStore.getState().language;
}
